const num1 = parseInt (prompt("ingrese un numero cualquiera"))

if (num1 % 2 === 0){
    document.writeln("<br>el numero es divisible en 2</br>")
}else if (num1 % 3 === 0) {
    document.writeln("el numero es divisible en 3")

} else if (num1 % 5 === 0) {
    document.writeln("el numero es divisible en 5")

} else if (num1 % 7 === 0) {
    document.writeln("el numero es divisible en 7")
}
    
    
